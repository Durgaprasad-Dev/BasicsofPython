import turtle
prasad_turtle = turtle.Turtle()
prasad_turtle.speed(50)
def square():

    prasad_turtle.forward(50)
    prasad_turtle.right(90)
    prasad_turtle.forward(50)
    prasad_turtle.right(90)
    prasad_turtle.forward(50)
    prasad_turtle.right(90)
    prasad_turtle.forward(50)

for count in range(200):
    square()